float adc_to_voltage(uint16_t adc_value, float vref, uint16_t adc_resolution) {
    float step_size = vref / (float)(1 << adc_resolution);
    return adc_value * step_size;
}

float voltage_to_kpa(float voltage, float vs) {
    float pressure = ((voltage - czpo) /sensitivity); // Sensitivity is 0.286 V/kPa
    return pressure;
}

float kpa_to_inh2o(float pressure_kpa) {
    const float kpa_to_inh2o_factor = 4.01474663203514;
    return pressure_kpa * kpa_to_inh2o_factor;
}


void autoZero() {
    HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1, 20);
    uint16_t adcValue = HAL_ADC_GetValue(&hadc1);
    float voltage = adc_to_voltage(adcValue, 3.3, 12); // Convert ADC value to voltage

    if (!initialized) {
        ema = voltage; // Initialize EMA with the first voltage reading
        initialized = true;
    } else {
        ema = alpha * voltage + (1 - alpha) * ema; // Update EMA
    }

    czpo = ema; // Assuming 2.5V is the ideal zero offset voltage
}
